// ++와 --연산의 개념을 이해하고 프로그램 작성
public class Main2 {

	public static void main(String[] args) {
		int a = 10;
		System.out.println("현재의 a는"+ a + "입니다.");
		a++;
		System.out.println("현재의 a는"+ a + "입니다.");
		System.out.println("현재의 a는"+ ++a + "입니다.");
		System.out.println("현재의 a는"+ a++ + "입니다.");
		System.out.println("현재의 a는"+ a + "입니다.");

	}

}
